// Instance Variable is known as Object level variable.
// Instance variables are declared in a class, but outside a method, constructor or any block.
package InstanceVariable;

public class ExampleOne {
	// Instance variable default value in 0 for Integer.
	// Instance variable default value in NULL for String.
	// Instance Variable declaration
    static int x;
    static String abc;
	public static void main(String[] args) {
    // Instance variable access in any static or main method when variable declared as STATIC
    System.out.println(+x);
    System.out.println(abc);
	}

}
