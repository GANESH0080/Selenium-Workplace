package SamplePrograms;

public class PrintFExample {

	public static void main(String[] args) {

		int i = 4;

		double j = 5;

		String str = "Ganesh";
		
		System.out.print("Output with print I:" + i);

		System.out.println("Output with println I:" + i);

		
		// The value of I is%d, the value of J is%f "the Integer "%d "as the value of I, and double "%f "to the value of J!
		// And "%s" is a string value point symbol meaning.
		System.out.printf("The value of I is %d, J value is %f, The value of str is %s", i, j, str);

	}

}
